  answer = gets.chomp.downcase
  if answer == ""
    playing = false
  elsif answer == states[i][:capital].downcase
    puts "That....is correct!"
    numRight +=1
  else
    puts "Wrong!"
    numWrong +=1

  end
  i +=1
end

puts "You answered #{numRight} correct!"
puts "Which means you got #{numWrong} incorrect"
puts "Would you like to play again? Y/N"
