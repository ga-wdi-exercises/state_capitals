def start(states)

  states.each do |state|
    state[:correct]=0
    state[:incorrect]=0
  end

  states.shuffle!
  states.each do |state|
    puts "What is the capital of #{state[:name]}?"
    answer = gets.chomp
    if answer.capitalize == state[:capital]
      puts "Correct!"
      state[:correct] += 1
    else
      puts "Wrong!"
      state[:answer] = "wrong"
      state[:incorrect] += 1
    end #answer logic
  end #state do loop
  corrects = states.map{|state| state[:correct]}.reduce(:+)
  incorrects = states.map{|state| state[:incorrect]}.reduce(:+)
  score = 50 - incorrects
  puts "Your score is #{score}. Great job! Do you want to play again (yes/no)?"
  play_again = gets.chomp
  if play_again.downcase == 'yes'
      new_state = states.sort_by{|state| state[:incorrect]}
      start(new_state)
  else
    puts "Thanks for playing!"
  end #play again statement
end #start of function

start(states)
